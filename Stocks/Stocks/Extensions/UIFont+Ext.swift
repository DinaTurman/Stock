import UIKit

extension UIFont {
    static func montserratBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Montserrat-Bold", size: size)
    }
    
    static func montserratSemiBold(size: CGFloat) -> UIFont? {
        return UIFont(name: "Montserrat-SemiBold", size: size) 
    }
}

